#include<bits/stdc++.h>
using namespace std;
int main() {
    // Integer --> Range - [10^-9, 10^9]
    int numInt = 10;
    cout << numInt << endl;

    // Long --> can store no's till 10^12
    long numLong = 100000000;
    cout << numLong << endl;

    // Long Long --> can store no's till 10^18
    long long numLongLong = 100000000000;
    cout << numLongLong << endl;

    // float --> can store upto 7 decimal places
    float numFloat = 1.1234567;
    cout << numFloat << endl;

    // double --> can store upto 15 decimal places
    double numDouble = 1.123456789123456;
    cout << numDouble << endl;

    // char --> all 256 characters
    char ch = 'a';
    cout << ch << endl;

    // string --> combination of characters
    string str = "TUF+";
    cout << str << endl;
    
    return 0;
}